#!/bin/bash
REPO_NAME="teacher-comments-generator"

echo "=========================================="
echo "🤖 第一階段：請 Gemini CLI 重構並加入新功能..."
echo "=========================================="

gemini-cli "你是一位資深前端架構師與 UI/UX 專家。我們目前有一個使用 React + Tailwind CSS 寫成的「台灣國小期末評語生成器」index.html。請在保留原本『左側批次匯入與名冊清單、右側單生微調設定、全域 LocalStorage API Key 密碼欄位、全域一鍵複製複製輸出面板』的Dashboard架構下，將程式碼完整重構並升級以下三個核心功能：

1. 【評語生成控制：支援重新生成與復原（Regenerate & Rollback）】
   - 在每個學生生成評語後的文字顯示區旁，新增一個「🔄 重新生成」按鈕。點擊後能單獨針對該學生以相同的特質配置重新呼叫 API 產生新文本。
   - 同時必須實作「返回上一次/復原」機制（保留前一次生成的文本狀態），讓老師如果覺得新生的沒有舊的好，可以一鍵還原回上一次的評語。

2. 【擴充多維度標籤矩陣（每類至少10個）與「自訂標籤（Custom Tags）」功能】
   - 請將原本的四大面向標籤大幅擴充，確保「每個分類領域內建至少 10 個」台灣小學教育現場的高頻質樸詞彙（例如：常規品格需包含：誠實負責、遵守紀律、愛惜公物、沉穩內鍊、粗心大意、情緒穩定、尊師重道、言行一致、熱心公務、有禮貌）。
   - 在標籤面板的最下方，為每個領域（或全域）新增一個「＋自訂標籤」的輸入框。老師可以自己輸入任意詞彙（例如：『愛打小報告』、『全班的開心果』），按下 Enter 或點擊新增後，該自訂標籤會動態以不同顏色（如淡藍色）加入標籤海中，且同樣可被勾選並納入 AI 生成的提示詞（Prompt）中。

3. 【新增「導師補充建議欄（Teacher's Note Area）」】
   - 在右側學生設定面板中，除了個性標籤與語氣外，再額外新增一欄獨立的文字框（Textarea），標題為「📝 導師補充備忘/特定事件建議」。
   - 允許老師手動輸入該學生的具體事蹟（例如：『期中考數學進步30分』、『擔任衛生股長認真負責』）。在呼叫 Gemini API 時，必須將此欄位內容作為高優先權的上下文（Context）餵給模型，讓生成的評語能真正做到量身打造、充滿真實細節。

【技術與視覺要求】
- 所有的狀態（新增的自訂標籤、補充建議內容、歷史評語紀錄）必須與學生的 ID/姓名 綁定在同一個獨立的 State 物件中，切換學生時不能遺失。
- 視覺維持優雅的莫蘭迪暖色調，並有清晰的 Loading 狀態與防呆提示。請直接輸出不省略、立即可執行的完整 index.html 程式碼。" > index.html

echo "✅ 網頁程式碼 index.html 已由 Gemini CLI 順利重構升級！"
echo ""
echo "=========================================="
echo "🛡️  第二階段：啟動 Gemini CLI 進行安全檢查..."
echo "=========================================="

CHECK_RESULT=$(gemini-cli "請幫我嚴格審查以下 index.html 的程式碼，特別檢查是否有硬編碼（Hard-coded）寫死任何 API Key。如果安全無虞，請回傳 'PASSED'。如果有資安風險，請指出行數並回傳 'FAILED'。" < index.html)

if [[ "$CHECK_RESULT" == *"FAILED"* ]]; then
    echo "❌ 檢查未通過！發現資安風險："
    echo "$CHECK_RESULT"
    exit 1
fi

echo "✅ 原始碼安全檢查通過 (No Hard-coded API Key Found)."
echo ""
echo "=========================================="
echo "🚀 第三階段：將全新功能自動推送至 GitHub..."
echo "=========================================="

git add index.html deploy.sh 2>/dev/null
git commit -m "feat: 升級重新生成、自訂標籤、與導師補充建議欄功能"
git push origin main

echo ""
echo "🎉 [升級成功] 新功能已全自動部署！"
GH_USER=$(gh api user --jq '.login')
echo "🔗 你的專屬正式網址將在 30 秒內完成無縫更新："
echo "👉 https://${GH_USER}.github.io/${REPO_NAME}/"
echo "=========================================="
